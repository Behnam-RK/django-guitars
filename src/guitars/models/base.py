from contextlib import nullcontext

from asgiref.sync import sync_to_async
from django.db import transaction
from django.db.models import DateTimeField
from django.db.models.base import Model
from django.db.models.functions import Now
from django.db.models.options import Options
from django.utils.functional import cached_property

from .soft_deletion import SoftDeletableModel


class DatedModel(Model):
    """Abstract model with auto-managed ``_created_at`` and ``_updated_at`` fields.

    Both default to ``NOW()`` at the database level. ``_updated_at`` is kept
    current by a PostgreSQL statement-level trigger (not Django signals),
    generated by ``makeguitarmigrations``. This means timestamps are
    accurate even for raw SQL or bulk operations that bypass ``.save()``.
    """

    _created_at = DateTimeField(verbose_name='Created at', db_default=Now(), editable=False)
    _updated_at = DateTimeField(verbose_name='Updated at', db_default=Now(), editable=False)

    class Meta:
        abstract = True


class UpdatableModel(Model):
    """Abstract model that adds a convenience ``.update(**attrs)`` method.

    Sets attributes and saves in one call with fine-grained control.
    """

    _meta: Options

    class Meta:
        abstract = True

    @cached_property
    def _updatable_fields(self) -> set[str]:
        fields = set()
        for field in self._meta._get_fields(reverse=False, include_hidden=True):
            fields.add(field.name)
            fields.add(field.column)  # To accept foreign keys in fk_id format!

        return fields

    def _prepare_update(
        self,
        _save: bool,
        _save_all_fields: bool,
        _raise_for_excessive: bool,
        attrs: dict[str, object],
    ) -> tuple[dict[str, object], set[str] | None]:
        """Validate attrs and set non-M2M fields in memory.

        Returns ``(m2m_attrs, update_fields)`` ready for the save step.
        ``update_fields`` is ``None`` when all fields should be saved.
        """
        fields = self._updatable_fields
        m2m_fields = {field.name for field in self._meta.local_many_to_many}
        given_fields = set(attrs.keys())
        excessive_fields = given_fields - fields
        updating_fields = (fields & given_fields) - m2m_fields

        if excessive_fields and _raise_for_excessive:
            raise ValueError(f'Invalid arguments: {excessive_fields}. (valid choices: {fields})')

        m2m_attrs: dict[str, object] = {}
        for attr, attr_value in attrs.items():
            if attr in m2m_fields:
                m2m_attrs[attr] = attr_value
            elif attr in updating_fields:
                setattr(self, attr, attr_value)

        if not _save and m2m_attrs:
            raise ValueError('Cannot update m2m fields without saving the instance!')

        update_fields = updating_fields if not _save_all_fields and updating_fields else None
        return m2m_attrs, update_fields

    def update(
        self,
        _save=True,
        _save_all_fields=False,
        _raise_for_excessive=True,
        _disable_signals=False,
        **attrs,
    ):
        """Set attributes on the instance and optionally persist to the database.

        Only the fields passed as ``**attrs`` are written to the DB (via
        ``update_fields``), unless ``_save_all_fields=True``.

        When called with ``_save=False``, attributes are set in memory only.
        A subsequent call with ``_save=True`` will **not** include those
        earlier attributes unless ``_save_all_fields=True`` is also passed.

        M2M fields are handled via ``.set(values, clear=True)`` and require
        ``_save=True``.

        The save runs inside ``transaction.atomic()``.
        """
        m2m_attrs, update_fields = self._prepare_update(
            _save, _save_all_fields, _raise_for_excessive, attrs
        )

        if _save:
            from guitars.signals import DisableSignals

            signals_context = DisableSignals() if _disable_signals else nullcontext()
            with signals_context:
                with transaction.atomic():
                    self.save(update_fields=update_fields)
                    for attr, attr_value in m2m_attrs.items():
                        getattr(self, attr).set(attr_value, clear=True)

    async def aupdate(
        self,
        _save=True,
        _save_all_fields=False,
        _raise_for_excessive=True,
        _disable_signals=False,
        **attrs,
    ):
        """Async version of ``.update()``. See ``.update()`` for full documentation."""
        await sync_to_async(self.update)(
            _save=_save,
            _save_all_fields=_save_all_fields,
            _raise_for_excessive=_raise_for_excessive,
            _disable_signals=_disable_signals,
            **attrs,
        )


class HasCachedPropertyModel(Model):
    """Abstract model that auto-invalidates ``@cached_property`` on refresh.

    Overrides ``refresh_from_db()`` to clear all cached properties, preventing
    stale in-memory values after a database refresh.
    """

    class Meta:
        abstract = True

    def expire_cached_properties(self, *properties):
        obj_dict = self.__class__.__dict__
        properties = properties or obj_dict.keys()
        for key in properties:
            if isinstance(obj_dict[key], cached_property):
                self.__dict__.pop(key, None)

    def refresh_from_db(self, *args, **kwargs):
        self.expire_cached_properties()
        return super().refresh_from_db(*args, **kwargs)


class DutarModel(UpdatableModel, HasCachedPropertyModel):
    """The two-string rung: the ``.update()`` helper (``UpdatableModel``) and
    cached-property invalidation (``HasCachedPropertyModel``), nothing else.

    No timestamps, no soft deletion — the lightest base, for models that just
    want the ergonomic helpers.
    """

    class Meta:
        abstract = True


class SetarModel(DatedModel, DutarModel):
    """The three-string rung: ``DutarModel`` plus database-managed timestamps
    (``DatedModel``).

    Adds ``_created_at`` / ``_updated_at`` on top of ``.update()`` and
    cached-property invalidation.

    Naming note: "se-tār" is Persian for "three strings", which is why this
    rung sits at three. The modern instrument actually has four — a fourth
    string was added in the 18th century (attributed to Moshtāq Ali Shāh).
    The ladder follows the etymology, not the current string count.
    """

    class Meta:
        abstract = True

    @classmethod
    def app_label(cls) -> str:
        """Returns app label in lowercase."""
        if not (hasattr(cls, '_meta') and cls._meta.app_label):
            raise AttributeError(f'{cls.__name__}._meta.app_label is not set!')

        return cls._meta.app_label

    @classmethod
    def model_name(cls) -> str:
        """Returns model name in lowercase"""
        if not (hasattr(cls, '_meta') and cls._meta.model_name):
            raise AttributeError(f'{cls.__name__}._meta.model_name is not set!')

        return cls._meta.model_name

    @classmethod
    def class_name(cls) -> str:
        """Returns class name"""
        return cls.__name__

    def __repr__(self) -> str:
        # Only concrete non-M2M fields — M2M / reverse relations on an unsaved
        # instance raise ValueError formatted with the instance itself, which
        # would recurse back into __repr__.
        representation = f'<{self.__class__.__name__} ID:{self.pk} - '
        for field in self._meta.fields:
            if not field.editable:
                continue
            value = getattr(self, field.name)
            if value is None:
                continue
            representation += f'{field.name}: {value} - '
        return representation + '>'


class GuitarModel(SetarModel, SoftDeletableModel):
    """The six-string rung: ``SetarModel`` plus PostgreSQL soft deletion.

    The recommended base for models that need timestamps, ``.update()``,
    cached-property invalidation, and soft deletion together. ``Meta``
    inherits ``SoftDeletableModel``'s soft-delete index and default manager.
    """

    class Meta(SoftDeletableModel.Meta):
        abstract = True
